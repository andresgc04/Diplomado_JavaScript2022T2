const fruta = "🍏";

switch(fruta)
{
    case "🥭":
        console.log("Mango 🥭");
        break;
    case "🍍":
        console.log("Piña 🍍");
        break;
    case "🍏":
        console.log("Manzana Verde 🍏");
        break;
    case "🍎":
        console.log("Manzana Roja 🍎");
        break;
    default:
        console.log("Esto no parece ser una fruta");
        break;
}