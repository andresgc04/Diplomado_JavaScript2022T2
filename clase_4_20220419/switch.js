/*Condicional Switch*/

const profesion = "Programador";

switch (profesion) {
  case "Maestro":
    console.log("La profesión seleccionada es de Maestro 🍎");
    break;
  case "Ingeniero":
    console.log("La profesión seleccioanda es de Ingeniero 👷‍♂️");
    break;
  case "Doctor":
    console.log("La Profesión seleccionada es de Doctor 👨‍⚕️");
    break;
  default:
    console.log("La Profesión no es valida 🤔");
    break;
}
